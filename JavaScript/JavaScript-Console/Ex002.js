var a = 5;
var b = 8;

a > b && b % 2 == 0; // False

a <= b || b / 2 == 2; // True

var média = 5.5;

média > 7 ? "APROVADO" : "REPROVADO"; // A média não é maior que 7 então vai mostra 'REPROVADO'
média += 3;

média > 7 ? "APROVADO" : "REPROVADO"; // A média é maior que 7 então vai mostra 'APROVADO'

